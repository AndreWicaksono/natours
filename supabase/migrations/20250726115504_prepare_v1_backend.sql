    LANGUAGE "plpgsql"
    SET search_path = ''
    AS $$
BEGIN
    RETURN json_build_object('server_time', TO_CHAR(NOW() AT TIME ZONE 'UTC', 'YYYY-MM-DD"T"HH24:MI:SS.US"Z"'));
END;
$$;

